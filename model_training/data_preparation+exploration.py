# --- 2. CORE FEATURE EXTRACTION FUNCTION ---
def extract_flow_features(flow_id, flow_data):
    """
    Computes statistical and volumetric features for a given time-window of traffic.
    Includes advanced symmetry metrics (byte_ratio, pkt_ratio).
    """
    src_ip, dst_ip, src_port, dst_port, protocol = flow_id

    fwd_lengths = flow_data['fwd_pkt_lengths']
    bwd_lengths = flow_data['bwd_pkt_lengths']
    all_lengths = fwd_lengths + bwd_lengths

    fwd_iats = flow_data['fwd_iats']
    bwd_iats = flow_data['bwd_iats']

    duration = flow_data['last_seen'] - flow_data['start_time']
    if duration <= 0: duration = 0.000001 # Prevent division by zero safely

    fwd_pkts = len(fwd_lengths)
    bwd_pkts = len(bwd_lengths)
    fwd_bytes = sum(fwd_lengths)
    bwd_bytes = sum(bwd_lengths)
    total_pkts = fwd_pkts + bwd_pkts
    total_bytes = fwd_bytes + bwd_bytes

    # Mathematical Ratios (The Silver Bullets for Tree Models)
    pkt_ratio = fwd_pkts / bwd_pkts if bwd_pkts > 0 else float(fwd_pkts)
    byte_ratio = fwd_bytes / bwd_bytes if bwd_bytes > 0 else float(fwd_bytes)

    features = {
        'src_ip': src_ip,
        'src_port': int(src_port),
        'dst_ip': dst_ip,
        'dst_port': int(dst_port),
        'protocol': int(protocol),
        'start_time': flow_data['start_time'],
        'end_time': flow_data['last_seen'],
        'duration': duration,

        'fwd_pkts': fwd_pkts,
        'bwd_pkts': bwd_pkts,
        'fwd_bytes': fwd_bytes,
        'bwd_bytes': bwd_bytes,
        'tot_pkts': total_pkts,
        'tot_bytes': total_bytes,

        'fwd_iat_mean': np.mean(fwd_iats) if fwd_iats else 0.0,
        'bwd_iat_mean': np.mean(bwd_iats) if bwd_iats else 0.0,
        'fwd_iat_max': np.max(fwd_iats) if fwd_iats else 0.0,
        'bwd_iat_max': np.max(bwd_iats) if bwd_iats else 0.0,

        'fwd_syn': flow_data['fwd_syn'],
        'fwd_fin': flow_data['fwd_fin'],
        'bwd_syn': flow_data['bwd_syn'],
        'bwd_fin': flow_data['bwd_fin'],

        # --- Computed Averages & Rates ---
        'avg_fwd_pkt_size': np.mean(fwd_lengths) if fwd_lengths else 0.0,
        'avg_bwd_pkt_size': np.mean(bwd_lengths) if bwd_lengths else 0.0,
        'avg_pkt_size': np.mean(all_lengths) if all_lengths else 0.0,
        'avg_pkts_per_sec': total_pkts / duration,
        'avg_bytes_per_sec': total_bytes / duration,
        'flow_byts_s': total_bytes / duration,

        # --- Engineered Symmetry Features ---
        'pkt_ratio': pkt_ratio,
        'byte_ratio': byte_ratio
    }
    return features

def process_pcap_in_chunks(pcap_path, output_csv, progress_tracker):
    """
    Reads a PCAP file iteratively, cutting active connections into 15-second windows.
    """
    print(f"{progress_tracker} Processing: {os.path.basename(pcap_path)}")
    active_flows = {}
    completed_chunks = []
    packet_count = 0

    try:
        with PcapReader(pcap_path) as pcap_reader:
            for pkt in pcap_reader:
                packet_count += 1
                if packet_count % 100000 == 0:
                    print(f"    ... scanned {packet_count:,} packets. Active flows in memory: {len(active_flows)}")

                if not pkt.haslayer(IP): continue

                ip_layer = pkt[IP]
                syn_flag, fin_flag = 0, 0

                if pkt.haslayer(TCP):
                    src_port, dst_port, proto = pkt[TCP].sport, pkt[TCP].dport, 6
                    tcp_flags = pkt[TCP].flags
                    if 'S' in tcp_flags: syn_flag = 1
                    if 'F' in tcp_flags: fin_flag = 1
                elif pkt.haslayer(UDP):
                    src_port, dst_port, proto = pkt[UDP].sport, pkt[UDP].dport, 17
                else:
                    continue

                src_ip, dst_ip = ip_layer.src, ip_layer.dst

                if src_ip < dst_ip:
                    flow_id = (src_ip, dst_ip, str(src_port), str(dst_port), str(proto))
                    direction = 'fwd'
                else:
                    flow_id = (dst_ip, src_ip, str(dst_port), str(src_port), str(proto))
                    direction = 'bwd'

                timestamp = float(pkt.time)
                pkt_len = len(pkt)

                if flow_id not in active_flows:
                    active_flows[flow_id] = {
                        'start_time': timestamp, 'last_seen': timestamp,
                        'fwd_pkt_lengths': [], 'bwd_pkt_lengths': [],
                        'fwd_iats': [], 'bwd_iats': [],
                        'fwd_syn': 0, 'fwd_fin': 0, 'bwd_syn': 0, 'bwd_fin': 0
                    }

                flow = active_flows[flow_id]

                if timestamp - flow['start_time'] >= CHUNK_WINDOW_SEC:
                    completed_chunks.append(extract_flow_features(flow_id, flow))
                    active_flows[flow_id] = {
                        'start_time': timestamp, 'last_seen': timestamp,
                        'fwd_pkt_lengths': [], 'bwd_pkt_lengths': [],
                        'fwd_iats': [], 'bwd_iats': [],
                        'fwd_syn': 0, 'fwd_fin': 0, 'bwd_syn': 0, 'bwd_fin': 0
                    }
                    flow = active_flows[flow_id]

                iat = timestamp - flow['last_seen']
                if direction == 'fwd':
                    flow['fwd_pkt_lengths'].append(pkt_len)
                    if len(flow['fwd_pkt_lengths']) > 1: flow['fwd_iats'].append(iat)
                    flow['fwd_syn'] += syn_flag
                    flow['fwd_fin'] += fin_flag
                else:
                    flow['bwd_pkt_lengths'].append(pkt_len)
                    if len(flow['bwd_pkt_lengths']) > 1: flow['bwd_iats'].append(iat)
                    flow['bwd_syn'] += syn_flag
                    flow['bwd_fin'] += fin_flag

                flow['last_seen'] = timestamp

                if packet_count % 10000 == 0:
                    dead_flows = [fid for fid, f in active_flows.items() if (timestamp - f['last_seen']) > IDLE_TIMEOUT_SEC]
                    for fid in dead_flows:
                        if len(active_flows[fid]['fwd_pkt_lengths']) + len(active_flows[fid]['bwd_pkt_lengths']) > 2:
                            completed_chunks.append(extract_flow_features(fid, active_flows[fid]))
                        del active_flows[fid]

        for fid, f in active_flows.items():
            if len(f['fwd_pkt_lengths']) + len(f['bwd_pkt_lengths']) > 2:
                completed_chunks.append(extract_flow_features(fid, f))

        if completed_chunks:
            df = pd.DataFrame(completed_chunks)
            df.to_csv(output_csv, index=False)
            print(f"    [+] Saved {len(df)} time-windows to {os.path.basename(output_csv)}\n")
        else:
            print("    [-] No valid traffic data found.\n")

    except Exception as e:
        print(f"    [!] Error processing {os.path.basename(pcap_path)}: {e}\n")


