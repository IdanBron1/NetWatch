"""To train an accurate Machine Learning model, we must group our raw traffic into logical classes. We are classifying the dataset into **4 categories**.

The categorization is not based on what the app *looks* like to a human, but rather how the mathematical shape of the data packets behaves on the network.

### 1. Streaming (Netflix, YouTube, Vimeo, Spotify)
* **Network Behavior:** Streaming platforms do not download data continuously. They download a large chunk of video/audio into a buffer, pause while the user consumes it, and then download another chunk.
* **Mathematical Signature:** High volume bursts of maximum-size packets, followed by distinct periods of zero traffic (pauses).

### 2. File Transfer (SCP, SFTP, Rsync, Skype File Transfer)
* **Network Behavior:** File transfers attempt to move a large block of data from Point A to Point B as fast as the network allows.
* **Mathematical Signature:** Extremely high `avg_bytes_per_sec`, highly asymmetrical (mostly downloading or mostly uploading), with **no intentional pauses**.

### 3. Real-Time Call (VoIP, Skype/Hangouts/Facebook Audio & Video)
* **Network Behavior:** Live two-way communication requires extremely low latency.
* **Mathematical Signature:** A steady, continuous, symmetrical stream of medium-sized packets. There are no massive spikes, but also no periods of total silence, creating a flat "heartbeat" of data.

### 4. Messaging & Async Comm (Email, Skype Chat, WhatsApp, IMAP/SMTP)
* **Network Behavior:** Both modern chat and email apps rely on persistent, dormant connections (like WebSockets or IMAP IDLE). They stay connected to a server to receive instant push notifications.
* **Mathematical Signature:** Very low baseline bandwidth consisting of regular, tiny "keep-alive" packets. This is occasionally interrupted by sudden, brief bursts of bandwidth when an actual text message, photo, or email attachment is sent or received.

***
**Note on True Infrastructure:** The AI is trained specifically on these human-driven applications. Low-level machine background noise (like DNS on Port 53 or NTP on Port 123) is handled separately in our live application using Hardcoded Heuristic Overrides, where it is classified strictly as "Infrastructure".
"""

