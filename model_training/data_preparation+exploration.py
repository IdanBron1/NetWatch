# --- 1. CONFIGURATION ---
CSV_INPUT_DIR = "/content/drive/MyDrive/Colab_Notebooks/pcaps/converted_CSVs"
MASTER_OUTPUT_FILE = "/content/drive/MyDrive/Colab_Notebooks/mit_unb_combined.csv"

